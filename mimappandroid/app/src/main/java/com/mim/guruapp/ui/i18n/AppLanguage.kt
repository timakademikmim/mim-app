package com.mim.guruapp.ui.i18n

import androidx.compose.runtime.Composable
import androidx.compose.runtime.staticCompositionLocalOf
import java.time.LocalDate
import java.time.YearMonth
import java.time.format.DateTimeFormatter
import java.time.format.TextStyle
import java.util.Locale

enum class AppLanguage(val code: String) {
  Indonesian("id"),
  English("en"),
  Arabic("ar");

  companion object {
    fun fromCode(code: String): AppLanguage {
      return when (code.trim().lowercase()) {
        "en" -> English
        "ar" -> Arabic
        else -> Indonesian
      }
    }
  }
}

val LocalAppLanguage = staticCompositionLocalOf { AppLanguage.Indonesian }

@Composable
fun t(text: String): String {
  return translateText(text, LocalAppLanguage.current)
}

@Composable
fun ti(text: String): String {
  return translateInfoText(text, LocalAppLanguage.current)
}

fun translateText(text: String, language: AppLanguage): String {
  if (language == AppLanguage.Indonesian) return text
  val dictionary = when (language) {
    AppLanguage.English -> englishTranslations
    AppLanguage.Arabic -> arabicTranslations
    AppLanguage.Indonesian -> emptyMap()
  }
  return dictionary[text] ?: text
}

fun translateInfoText(text: String, language: AppLanguage): String {
  val direct = translateText(text, language)
  if (direct != text || language == AppLanguage.Indonesian) return direct
  val clean = text.trim()
  return when {
    clean.startsWith("Jadwal mengajar reguler guru pada ") -> {
      val dateText = clean
        .removePrefix("Jadwal mengajar reguler guru pada ")
        .trim()
      when (language) {
        AppLanguage.English -> "Regular teacher teaching schedule on $dateText"
        AppLanguage.Arabic -> "\u062C\u062F\u0648\u0644 \u062A\u062F\u0631\u064A\u0633 \u0639\u0627\u062F\u064A \u0641\u064A $dateText"
        AppLanguage.Indonesian -> clean
      }
    }
    clean.startsWith("Sakit ") && clean.contains(" hari, izin ") -> {
      val replaced = clean
        .replace("Sakit ", "${translateText("Sakit", language)} ")
        .replace(" hari, izin ", " ${translateText("hari", language)}, ${translateText("Izin", language)} ")
        .replace(" hari", " ${translateText("hari", language)}")
      replaced
    }
    clean.matches(Regex("""\d+\s+jadwal.*""")) -> {
      clean.replace("jadwal", translateText("jadwal", language))
    }
    clean.startsWith("Otomatis dari sumber data. ") -> {
      val rest = clean.removePrefix("Otomatis dari sumber data. ")
      "${translateText("Otomatis dari sumber data.", language)} ${translateInfoText(rest, language)}"
    }
    else -> clean
  }
}

fun localeForLanguage(language: AppLanguage): Locale {
  return when (language) {
    AppLanguage.English -> Locale.ENGLISH
    AppLanguage.Arabic -> Locale.forLanguageTag("ar")
    AppLanguage.Indonesian -> Locale.forLanguageTag("id-ID")
  }
}

@Composable
fun appLocale(): Locale {
  return localeForLanguage(LocalAppLanguage.current)
}

fun formatDateForLanguage(date: LocalDate, pattern: String, language: AppLanguage): String {
  return date.format(DateTimeFormatter.ofPattern(pattern, localeForLanguage(language)))
}

fun formatMonthForLanguage(month: YearMonth, pattern: String, language: AppLanguage): String {
  return month.atDay(1).format(DateTimeFormatter.ofPattern(pattern, localeForLanguage(language)))
}

fun dayNameForLanguage(date: LocalDate, style: TextStyle, language: AppLanguage): String {
  return date.dayOfWeek.getDisplayName(style, localeForLanguage(language))
}

